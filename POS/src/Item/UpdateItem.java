    private void itemtxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemtxtActionPerformed
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/thogakade", "root", "sithum");
            String SQL="Select * from item where code=?";
            PreparedStatement stm = connection.prepareStatement(SQL);
            String id=itemtxt.getText();
            stm.setObject(1, id);
            ResultSet rst = stm.executeQuery();
            if(rst.next()){
                destxt.setText(rst.getString("description"));
                utxt.setText(rst.getString("UnitPrice"));
                qtxt.setText(rst.getString("qtyOnHand"));
            }else{
                JOptionPane.showMessageDialog(this, "No Item found..");
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(UpdateForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(UpdateForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_itemtxtActionPerformed

